"""四則演算の基本的な使い方"""

# 足し算
print(5 + 3)  # 8

# 引き算
print(5 - 3)  # 2

# 掛け算
print(5 * 3)  # 15

# 割り算
print(5 / 3)  # 1.6666...


"""他の色々な計算"""
# べき乗 （累乗）
print(2**3)  # 2 の 3 乗 = 8

# 余りを求める （剰余演算）
print(7 % 3)  # 7 ÷ 3 の余り = 1

# 整数の商を求める （切り捨て除算）
print(7 // 3)  # 7 ÷ 3 の商 = 2


"""計算の優先順位の例"""
print(2 + 3 * 4)  # 14 (3 * 4 が先)
print((2 + 3) * 4)  # 20 (2 + 3 が先)


"""よくある計算例"""
# 平均値を求める
print((80 + 95 + 90) / 3)  # 88.33...

# 消費税を計算する （10%）
print(1000 * 0.1)  # 100

# 割引後の価格を計算する （20% オフ）
print(1000 * 0.8)  # 800
