"""print 関数の基本的な使い方"""

print("Hello, World!")  # 文字列を表示 - Hello, World!
print(42)  # 数値を表示 - 42
print("私は", 16, "歳です")  # 複数の値を表示 - 私は 16 歳です


"""文字列の表示の例"""
print("こんにちは")  # こんにちは
print("Hello")  # Hello


"""数値の表示の例"""
print(42)  # 42
print(3 + 5)  # 8


"""複数の値を表示する例"""
print("私の名前は", "太郎", "です")  # 私の名前は 太郎 です
print("計算結果:", 3 * 4)  # 計算結果: 12
